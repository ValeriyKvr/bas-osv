with st.expander("7. Фінансові коефіцієнти: формули та нормативи"):
    st.markdown("""
**Ліквідність** — чи вистачить оборотних коштів на покриття поточних боргів:

| Коефіцієнт | Формула | Норматив |
|---|---|---|
| Покриття (поточна ліквідність) | Оборотні активи / Поточні зобов'язання | ≥ 1,5 |
| Швидка ліквідність | (Оборотні − Запаси) / Поточні зобов'язання | ≥ 0,6 |
| Абсолютна ліквідність | Грошові кошти / Поточні зобов'язання | ≥ 0,2 |

**Фінансова стійкість** — за чий рахунок працює підприємство:

| Коефіцієнт | Формула | Норматив |
|---|---|---|
| Автономії | Власний капітал / Активи | ≥ 0,5 |
| Фінансової залежності | (Довгострокові + Поточні зобов'язання) / Активи | ≤ 0,5 |
| Робочий капітал, грн | Оборотні активи − Поточні зобов'язання | > 0 |

**Ділова активність** — швидкість обороту грошей. Береться **середній**
залишок за період: (сальдо на початок + сальдо на кінець) / 2, і кількість
днів у періоді (квартал ≈ 90, місяць ≈ 30):

```
DSO (днів) = Середня дебіторська (36, 37 Дт) × Дні періоду / Оборот Кт 70 (виручка)
DPO (днів) = Середня кредиторська (63 Кт)   × Дні періоду / Оборот Кт 63 (закупівлі)
DIO (днів) = Середні запаси (клас 2)        × Дні періоду / Оборот Дт 90 (собівартість)

Фінансовий цикл = DSO + DIO − DPO
```

*Як читати:* DSO 151 — покупці платять у середньому через 151 день після
відвантаження. Фінансовий цикл 221 день — стільки днів власні гроші
«заморожені» у запасах і дебіторці, поки не повернуться на рахунок;
що менше — то краще.

**Рентабельність продажів** = Фінрезультат періоду (зміна рах. 44) /
Виручка (Кт 70) × 100 %.

**Z″-рахунок Альтмана** — інтегральна оцінка фінансового здоров'я
(модель для непублічних компаній):

```
Z″ = 6,56·X1 + 3,26·X2 + 6,72·X3 + 1,05·X4

X1 = Робочий капітал / Активи          X3 = Фінрезультат періоду / Активи
X2 = Нерозподілений прибуток / Активи  X4 = Власний капітал / Зобов'язання
```

Z″ > 2,6 — безпечна зона; 1,1–2,6 — «сіра» зона; < 1,1 — зона ризику.
Оцінка **орієнтовна**: модель статистична і не замінює аналіз причин.

Кольори в таблиці: 🟩 у нормі, 🟨 потребує уваги, 🟥 поза нормативом.
Нормативи — загальноприйняті орієнтири; для харчового виробництва
доцільно з часом відкалібрувати їх по галузі.
""")

