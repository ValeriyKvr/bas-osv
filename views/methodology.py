with st.expander("3. Сторінка «Порівняння періодів»: відхилення та KPI"):
    st.markdown("""
Для двох обраних періодів — **базового (A)** і **порівнюваного (B)** — кожен
показник X порівнюється так:

```
Відхилення:      Δ  = X(B) − X(A)
Темп приросту:   Δ% = (X(B) − X(A)) / |X(A)| × 100 %
```

Якщо в базовому періоді показник нульовий, замість відсотка пишеться
**«нове»**; якщо нульовим став у порівнюваному — **«зникло»**.

Звідки беруться картки-показники (KPI) угорі сторінки:

| Показник | Джерело з ОСВ |
|---|---|
| Дохід | оборот **Кт рахунку 70** (з усіма субрахунками; містить ПДВ) |
| Собівартість | оборот **Дт рахунку 90** |
| Адмін. + збут | обороти **Дт 92 + Дт 93** |
| Фінрезультат періоду | **зміна нетто-сальдо рах. 44**: (Кт−Дт на кінець) − (Кт−Дт на початок) |
| Надходження / Платежі | обороти **Дт / Кт рахунків 30, 31, 33** за статтями руху коштів |
| Гроші на кінець | сальдо **30 + 31 + 33** на кінець періоду |
| Борг покупців | нетто-сальдо **рах. 36** на кінець (Дт − Кт) |

Чому фінрезультат рахується через рахунок 44, а не через 79: обороти 79
закриваються «в нуль» щоперіоду, тож прибуток саме періоду видно як
**приріст нерозподіленого прибутку** (рах. 44) за цей період. Це той самий
результат, що у Звіті про фінансові результати після податку.
""")

