with st.expander("6. Сторінка «Фінансовий стан»: балансові агрегати"):
    st.markdown("""
Коефіцієнти рахуються не з форми № 1 (її в базі може не бути), а **з тих
самих сальдо ОСВ**, згрупованих у балансові агрегати:

| Агрегат | Як рахується |
|---|---|
| Необоротні активи | нетто класу **1** (знос 13 автоматично мінусується) |
| Запаси | нетто класу **2** |
| Грошові кошти | нетто **30 + 31 + 33** |
| Дебіторська заборгованість | **дебетові** сальдо рахунків **36 і 37** |
| Оборотні активи | запаси + **усі дебетові** сальдо класів **3 і 6** |
| Власний капітал | нетто класу **4** (Кт − Дт) |
| Довгострокові зобов'язання | нетто класу **5** |
| Поточні зобов'язання | **кредитові** сальдо класу **6** + кредитові сальдо класу 3 (аванси отримані) |

**Розгорнутий принцип** для розрахункових класів 3 і 6: дебетовий залишок
(нам винні, переплата податку) — це **актив**, кредитовий (винні ми,
аванс від покупця) — **пасив**. Згортати їх між собою не можна — інакше
занижуються і активи, і зобов'язання, а коефіцієнти виходять прикрашеними.

**Вбудований контроль:** після групування завжди перевіряється тотожність

```
АКТИВИ = ВЛАСНИЙ КАПІТАЛ + ДОВГОСТРОКОВІ + ПОТОЧНІ ЗОБОВ'ЯЗАННЯ
```

На поточних даних розбіжність — 0,00 грн на кожну дату. Якщо вона
з'явиться — у періоді є незакриті рахунки класів 7–9 (період ще не
закритий в обліку) або нові рахунки поза класами 1–6.
""")

