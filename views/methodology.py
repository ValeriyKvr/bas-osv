with st.expander("8. Контролі коректності та звірка з 1С"):
    st.markdown("""
Що перевіряється автоматично і що легко перевірити вручну:

1. **Деталізація = зведена.** Сума рядків деталізації по рахунку (по всіх
   субконто) дорівнює рядку цього рахунку у зведеній ОСВ — до копійки.
2. **Рядок «Разом»** збалансований: підсумок Дт = підсумку Кт (по
   балансових рахунках).
3. **Балансова тотожність агрегатів** (розділ 6) — розбіжність 0,00.
4. **Звірка з 1С:** сформуйте в 1С звичайну ОСВ за той самий період по тій
   самій організації — рядки рахунків мають збігтися зі сторінкою «ОСВ»
   дашборда. Розбіжність можлива лише якщо в базі щось допроводили після
   вивантаження — тоді просто повторіть завантаження періоду на сторінці
   «Завантаження з 1С» (старі дані періоду перезаписуються повністю).

**Межі періоду.** Період береться строго з 00:00:00 першого дня по
23:59:59 останнього. Документи реформації 31 грудня належать до старого
року і в 1-й квартал наступного не потрапляють.
""")

