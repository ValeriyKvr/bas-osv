with st.expander("2. Сторінка «ОСВ (відомість)»: як збирається ієрархія"):
    st.markdown("""
Проводки в базі є лише по **рахунках нижнього рівня** (631, 6851, 311 тощо).
Рядки-групи (63, 6, клас у цілому) дашборд добудовує сам — **знизу вгору по
дереву плану рахунків**: показник групи дорівнює сумі показників усіх її
підрахунків плюс власні проводки (якщо є).

```
Оборот(63) = Оборот(631) + Оборот(632) + …
Оборот(6)  = Оборот(60) + Оборот(63) + Оборот(64) + …
```

**Згортання сальдо.** У таблиці сальдо показується згорнуто — як у
стандартній ОСВ 1С:

- рахуємо нетто: `С = Сальдо Дт − Сальдо Кт`;
- для **активних** рахунків: якщо С ≥ 0 — показуємо в колонці Дт,
  якщо С < 0 — у колонці Кт;
- для **пасивних** — дзеркально (нетто = Кт − Дт, показуємо в Кт);
- для **активно-пасивних** (631, 361, 64…) — нетто за знаком.

*Приклад:* по рахунку 631 сальдо Дт 100 000 (переплати постачальникам)
і Кт 250 000 (наші борги). Нетто = 100 000 − 250 000 = −150 000 →
у відомості рядок 631 покаже **Кт 150 000**.

**Рядок «Разом»** — сума по балансових рахунках (забалансові, як-от МЦ чи 01,
не входять — інакше підсумок Дт не зійшовся б із Кт).
""")

