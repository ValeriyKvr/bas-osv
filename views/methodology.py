with st.expander("4. Рух грошей за статтями: «на що платимо»"):
    st.markdown("""
Кожен банківський рух у базі має аналітику **«Стаття руху грошових коштів»**
(«товар от поставщиков», «налоги», «выплата зараб платы»…). Дашборд групує
обороти рахунків **30, 31, 33** по цій статті:

- **Надходження** = оборот **Дт** по статті (гроші прийшли на рахунок);
- **Платежі** = оборот **Кт** по статті (гроші пішли з рахунку).

**Внутрішні перекази** (стаття типу «переведення коштів на додатковий
рахунок») — це рух між власними рахунками підприємства: та сама сума
потрапляє і в надходження, і в платежі, штучно завищуючи обидва підсумки.
Тому за замовчуванням вони **виключені** (прапорець у бічній панелі) —
залишаються лише реальні розрахунки із зовнішнім світом.

*Перевірка вручну:* сума всіх статей (разом із внутрішніми) має збігтися
з оборотами Дт і Кт рядка 31 (плюс 30, 33) у відомості за той самий період.
""")

